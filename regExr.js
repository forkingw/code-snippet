/**
 * 支持汉字、字母、数字，不支持标点符号
 */
 export const noPunctuationReg = /^[\u4e00-\u9fa5a-zA-Z0-9]*$/
 /**
  * 仅支持汉字和字母
  */
 export const chineseLetterReg = /^[\u4e00-\u9fa5a-zA-Z]*$/
 /**
  * 邮箱
  */
 export const email = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
 
 /**
  * 统一社会信用代码(宽松匹配)(15位/18位/20位数字/字母)
  */
 export const creditCodeReg = /^(([0-9A-Za-z]{15})|([0-9A-Za-z]{18})|([0-9A-Za-z]{20}))$/
 
 /**
  * 身份证号
  * 支持 1代(15位)，2代（18位） 身份证
  */
 export const identityCardReg = /^\d{6}((((((19|20)\d{2})(0[13-9]|1[012])(0[1-9]|[12]\d|30))|(((19|20)\d{2})(0[13578]|1[02])31)|((19|20)\d{2})02(0[1-9]|1\d|2[0-8])|((((19|20)([13579][26]|[2468][048]|0[48]))|(2000))0229))\d{3})|((((\d{2})(0[13-9]|1[012])(0[1-9]|[12]\d|30))|((\d{2})(0[13578]|1[02])31)|((\d{2})02(0[1-9]|1\d|2[0-8]))|(([13579][26]|[2468][048]|0[048])0229))\d{2}))(\d|X|x)$/

/**
* 手机号【宽松】
* 只要是13,14,15,16,17,18,19开头即可
*/
export const phoneReg = /^(?:(?:\+|00)86)?1[3-9]\d{9}$/

/**
 * 座机
 */
export const telephoneReg = /^(?:(?:\d{3}-)?\d{8}|^(?:\d{4}-)?\d{7,8})(?:-\d+)?$/
